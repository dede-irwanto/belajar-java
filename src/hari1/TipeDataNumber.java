package hari1;

public class TipeDataNumber {
    public static void main(String[] args) {

        // Kode: Integer Number
        byte iniByte = 100; // size 1 byte
        short iniShort = 1000; // size 2 byte
        int iniInteger = 10000000; // size 4 byte
        long iniLong = 100000000; // size 8 byte
        long iniLong2 = 1000000000L;

        // Kode: Floating Point Number
        float iniFloat = 10.10F;
        double iniDouble = 10.10;

        // Kode: Literals
        int decimalInt = 34;
        int hexaDecimal = 0xFFFFFF; // tambahkan 0x
        int binaryDecimal = 0b10101010; // tambahkan 0b

        // Kode: Underscore
        int amount = 1_000_000_000; // digunakan untuk memudahkan pembacaan angka

    }
}
